#include <libft.h>

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*p;

	p = s;
	i = 0;
	while (i < n)
	{
		*p = (char) c;
		p++;
		i++;
	}
	return (s);
}
